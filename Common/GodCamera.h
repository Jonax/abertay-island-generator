//////////////////////////////////////////////////////////////////////////////////////////
//	GOD CAMERA CLASS																	//
//	Handles the prototype's god camera.													//
//////////////////////////////////////////////////////////////////////////////////////////

#ifndef _GODCAMERA_H_
#define _GODCAMERA_H_

//////////////////////////////////////////////////////////////////////////////////////////
//	EXTRA INCLUDE FILES
//////////////////////////////////////////////////////////////////////////////////////////
#include "GLCamera.h"		// OpenGL Camera class.  

//////////////////////////////////////////////////////////////////////////////////////////
//	CLASS DECLARATIONS
//	Declarations of the class and its members.  
//////////////////////////////////////////////////////////////////////////////////////////
class GodCamera
{
	public:
		GodCamera();		// Constructor. 
		~GodCamera();		// Destructor.  

		void Look();		// Sets the OpenGL camera to use this camera.  
		void Zoom();		// Zooms the camera in & out.  

		// Processes mouse input.  
		void ProcessMouseInput(POINT MousePos, POINT OldMouse);

		void SetRotate(bool lock);		// Sets the rotation lock.  
		void SetZoom(bool lock);		// Sets the zoom lock.  

	private:
	//////////////////////////////////////////////////////////////////////////////////////
	//	CLASS OBJECTS
	//////////////////////////////////////////////////////////////////////////////////////
		GLCamera *Camera;		// OpenGL camera class.  

	//////////////////////////////////////////////////////////////////////////////////////
	//	CLASS DATA
	//////////////////////////////////////////////////////////////////////////////////////
		GLfloat angle;			// Current angle of the camera in relation to the island.  
		GLfloat zoom;			// Zoom scale of the camera.  

		bool rotate_lock;		// Bool for locking rotation.  
		bool zoom_lock;			// Bool for locking zooming.  
};

#endif