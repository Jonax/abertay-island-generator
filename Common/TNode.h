//////////////////////////////////////////////////////////////////////////////////////////
//	TERRAIN NODE CLASS CODE																//
//	Used as a means to hold position & texture coordinate data for an OpenGL vertex.	//
//	Used as a base class for the terrain & the skydome.									//
//////////////////////////////////////////////////////////////////////////////////////////

#ifndef _TNODE_H_
#define _TNODE_H_

//////////////////////////////////////////////////////////////////////////////////////////
//	EXTRA INCLUDE FILES
//////////////////////////////////////////////////////////////////////////////////////////
#include "GLNode.h"			// OpenGL Node class.

//////////////////////////////////////////////////////////////////////////////////////////
//	CLASS DECLARATIONS
//	Declarations of the class and its members.  The class is derived from the OpenGL Node
//	class.
//////////////////////////////////////////////////////////////////////////////////////////
class TerrainNode : public GLNode
{
	public:
		// Constructors to create a terrain node.
		TerrainNode();
		TerrainNode(GLfloat x, GLfloat y, GLfloat z);

		void Draw();								// Renders the terrain node.  

		void SetNormal(GLfloat x, GLfloat y, GLfloat z);	// Sets the node's normal.  

	//////////////////////////////////////////////////////////////////////////////////////
	//	CLASS OBJECTS
	//////////////////////////////////////////////////////////////////////////////////////
		GLVertex Normal;							// Normal for the Node.
};

#endif