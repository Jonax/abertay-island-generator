//////////////////////////////////////////////////////////////////////////////////////////
//	2D VERTEX CLASS CODE																//
//	Used as a means to hold primitive data for OpenGL vertices in a fashion where		//
//	mathematical calculations between vertices can be performed in a contained manner.	//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//	VERTEX CLASS HEADER
//	Links to the GLVertex2's declarations.  
//////////////////////////////////////////////////////////////////////////////////////////
#include "GLVertex2.h"

//////////////////////////////////////////////////////////////////////////////////////////
//	VERTEX CONSTRUCTORS
//	Sets up the co-ordinates for a vertex based on what data is defined during the
//	vector's initialisation.  
//////////////////////////////////////////////////////////////////////////////////////////
//	Generic constructor.  Creates a vertex at (0, 0).
//////////////////////////////////////////////////////////////////////////////////////////
GLVertex2::GLVertex2()
{
	this->x = 0.0f;
	this->y = 0.0f;
}

//	Constructor.  Creates a vertex at (x, y).
//////////////////////////////////////////////////////////////////////////////////////////
GLVertex2::GLVertex2(GLfloat x, GLfloat y)
{
	this->x = x;
	this->y = y;
}

//	Function to assign values for the vertex based on another GLVertex2.
//////////////////////////////////////////////////////////////////////////////////////////
void GLVertex2::operator = (const GLVertex2 &rhs)
{
	this->x = rhs.x;
	this->y = rhs.y;
}