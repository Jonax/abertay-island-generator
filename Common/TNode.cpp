//////////////////////////////////////////////////////////////////////////////////////////
//	TERRAIN NODE CLASS CODE																//
//	Used as a means to hold position & texture coordinate data for an OpenGL vertex.	//
//	Used as a base class for the terrain & the skydome.									//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//	CLASS HEADER
//////////////////////////////////////////////////////////////////////////////////////////
#include "TNode.h"

//	Generic constructor.  Creates a node at (0, 0, 0).
//////////////////////////////////////////////////////////////////////////////////////////
TerrainNode::TerrainNode()
: GLNode()
{
	this->Normal = GLVertex();
}

//	Constructor.  Creates a node at (x, y, z).
//////////////////////////////////////////////////////////////////////////////////////////
TerrainNode::TerrainNode(GLfloat x, GLfloat y, GLfloat z)
: GLNode(x, y, z)
{
	this->Normal = GLVertex();
}

//	Function to render the terrain node.  
//////////////////////////////////////////////////////////////////////////////////////////
void TerrainNode::Draw()
{
	glNormal3f(Normal.x, Normal.y, Normal.z);
	glVertex3f(Position.x,	Position.y,	Position.z);
}

//	Function to set the normal of the node.  
//////////////////////////////////////////////////////////////////////////////////////////
void TerrainNode::SetNormal(GLfloat x, GLfloat y, GLfloat z)
{
	this->Normal = GLVertex(x, y, z);
}